
public class FloatType {

	public static void main(String[] args) {
		float f1 = 3.1416F;
		float f2 = 3.14e38f; // 科学计数法表示的3.4x10的38次方
		double d = 1.79e308;
		double d2 = -1.79e308;
		double d3 = 4.9e-324;

		System.out.println(f1);
		System.out.println(f2);
		System.out.println(d);
		System.out.println(d2);
		System.out.println(d3);
	}

}
